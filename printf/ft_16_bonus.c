#include "libftprintf.h"

int	ft_16(unsigned long int n)
{
	int	k;
	int	j;

	k = 0;
	if (n == 0)
		return (1);
	while (n > 0)
	{
		j = n % 16;
		n = n / 16;
		k++;
	}
	if (n < 0)
		return (8);
	return (k);
}
