#include "libftprintf.h"

int	ft_putstr(char *s, int	n)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (n > 0 && *s)
	{
		i += ft_putchar(*s);
		s++;
		n--;
	}
	return (i);
}
