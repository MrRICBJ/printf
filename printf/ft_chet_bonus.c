#include "libftprintf.h"

int	ft_chet(char *s, int *i)
{
	int	k;

	k = 0;
	if (s[*i] == '.')
		*i += 1;
	while (s[*i] >= '0' && s[*i] <= '9')
	{
		k = k * 10 + (s[*i] - '0');
		*i += 1;
	}
	*i -= 1;
	return (k);
}
