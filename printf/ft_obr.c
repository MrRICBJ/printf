#include "libftprintf.h"

int	ft_obr(char *s)
{
	int	i;
	int	k;

	k = 0;
	i = ft_strlen(s);
	k = i;
	i--;
	while (i >= 0)
	{
		write (1, &s[i], 1);
		i--;
	}
	return (k);
}
